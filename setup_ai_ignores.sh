write_file() {
  local path="$1"
  local content="$2"
  local dir

  dir="$(dirname "$path")"

  if [[ "$DRY_RUN" -eq 1 ]]; then
    if [[ -e "$path" ]]; then
      log "Would update: $path"
    else
      log "Would create: $path"
    fi
    return 0
  fi

  mkdir -p "$dir"

  if [[ -e "$path" && "$FORCE" -ne 1 ]]; then
    warn "Skipping existing file: $path (use --force to overwrite)"
    return 0
  fi

  if [[ -e "$path" && "$BACKUP" -eq 1 ]]; then
    cp "$path" "$path.bak"
    log "Backed up: $path.bak"
  fi

  printf '%s\n' "$content" > "$path"
  log "Wrote: $path"
  record_written_file "$path"
}

merge_ignore_content() {
  local path="$1"
  local generated_content="$2"
  local gitignore_rules=""
  local existing_rules=""

  if [[ -f ".gitignore" ]]; then
    gitignore_rules="$(awk 'NF && $0 !~ /^[[:space:]]*#/' ".gitignore")"
  fi

  if [[ -f "$path" ]]; then
    existing_rules="$(awk 'NF && $0 !~ /^[[:space:]]*#/' "$path")"
  fi

  {
    if [[ -n "$gitignore_rules" ]]; then
      printf '# Imported rules from .gitignore\n'
      printf '%s\n' "$gitignore_rules"
      printf '\n'
    fi

    if [[ -n "$existing_rules" ]]; then
      printf '# Preserved local rules from %s\n' "$path"
      printf '%s\n' "$existing_rules"
      printf '\n'
    fi

    printf '%s\n' "$generated_content"
  } | awk '
    BEGIN { previous_blank = 1 }
    {
      if ($0 == "") {
        if (!previous_blank) {
          print ""
          previous_blank = 1
        }
        next
      }

      previous_blank = 0

      if (!seen[$0]++) {
        print $0
      }
    }
  '
}

write_ignore_file() {
  local path="$1"
  local generated_content="$2"
  local dir
  local merged_content
  local current_content=""

  dir="$(dirname "$path")"
  merged_content="$(merge_ignore_content "$path" "$generated_content")"

  if [[ -f "$path" ]]; then
    current_content="$(cat "$path")"
  fi

  if [[ "$DRY_RUN" -eq 1 ]]; then
    if [[ ! -e "$path" ]]; then
      log "Would create merged ignore file: $path"
    elif [[ "$current_content" == "$merged_content" ]]; then
      log "Would keep unchanged: $path"
    else
      log "Would merge and update: $path"
    fi
    return 0
  fi

  mkdir -p "$dir"

  if [[ -e "$path" && "$current_content" == "$merged_content" ]]; then
    log "Unchanged: $path"
    return 0
  fi

  if [[ -e "$path" && "$BACKUP" -eq 1 ]]; then
    cp "$path" "$path.bak"
    log "Backed up: $path.bak"
  fi

  printf '%s\n' "$merged_content" > "$path"
  log "Wrote merged ignore file: $path"
  record_written_file "$path"
}

prompt_global_gitignore_update() {
  local answer
  local global_gitignore
  local existing_rules=""
  local path
  local appended=0

  [[ "$DRY_RUN" -eq 0 ]] || return 0
  [[ "${#WRITTEN_FILES[@]}" -gt 0 ]] || return 0
  [[ -t 0 && -t 1 ]] || return 0

  printf 'Add generated files to the global gitignore? [y/N] '
  read -r answer || return 0

  case "$answer" in
    y|Y|yes|YES) ;;
    *) return 0 ;;
  esac

  global_gitignore="$(git config --global --get core.excludesfile || true)"
  if [[ -z "$global_gitignore" ]]; then
    global_gitignore="$HOME/.gitignore_global"
    git config --global core.excludesfile "$global_gitignore"
    log "Configured global gitignore: $global_gitignore"
  fi

  mkdir -p "$(dirname "$global_gitignore")"
  touch "$global_gitignore"
  existing_rules="$(awk 'NF && $0 !~ /^[[:space:]]*#/' "$global_gitignore")"

  for path in "${WRITTEN_FILES[@]}"; do
    if ! grep -Fqx "$path" <<< "$existing_rules"; then
      printf '%s\n' "$path" >> "$global_gitignore"
      appended=1
      log "Added to global gitignore: $path"
    fi
  done

  if [[ "$appended" -eq 0 ]]; then
    log "Global gitignore already contains all generated file rules."
  fi
}

parse_args() {
  while [[ $# -gt 0 ]]; do
    case "$1" in
      --profile)
        [[ $# -ge 2 ]] || die "--profile requires a value"
        PROFILE="$2"
        shift 2
        ;;
      --tools)
        [[ $# -ge 2 ]] || die "--tools requires a value"
        TOOLS_CSV="$2"
        shift 2
        ;;
      --force)
        FORCE=1
        shift
        ;;
      --backup)
        BACKUP=1
        shift
        ;;
      --dry-run)
        DRY_RUN=1
        shift
        ;;
      -h|--help)
        usage
        exit 0
        ;;
      *)
        die "Unknown argument: $1"
        ;;
    esac
  done

  case "$PROFILE" in
    balanced|strict) ;;
    *) die "Invalid profile: $PROFILE" ;;
  esac
}

parse_tools() {
  local raw="$1"
  local normalized=()
  local item
  local tool

  IFS=',' read -r -a REQUESTED <<< "$raw"
  for item in "${REQUESTED[@]}"; do
    item="${item//[[:space:]]/}"
    [[ -n "$item" ]] || continue

    tool="$(normalize_tool "$item")" || die "Unsupported tool: $item"
    if [[ "$tool" == "all" ]]; then
      normalized=(gemini qwen windsurf jetbrains claude codex copilot)
      break
    fi

    if ! contains_tool "$tool" "${normalized[@]:-}"; then
      normalized+=("$tool")
    fi
  done

  [[ "${#normalized[@]}" -gt 0 ]] || die "No tools selected"
  SELECTED_TOOLS=("${normalized[@]}")
}

generate_files() {
  local ignore_content
  ignore_content="$(build_ignore_content)"

  if contains_tool "gemini" "${SELECTED_TOOLS[@]}"; then
    write_ignore_file ".geminiignore" "$ignore_content"
  fi

  if contains_tool "qwen" "${SELECTED_TOOLS[@]}"; then
    write_ignore_file ".qwenignore" "$ignore_content"
  fi

  if contains_tool "windsurf" "${SELECTED_TOOLS[@]}"; then
    write_ignore_file ".codeiumignore" "$ignore_content"
  fi

  if contains_tool "jetbrains" "${SELECTED_TOOLS[@]}"; then
    write_ignore_file ".aiignore" "$ignore_content"
  fi

  if contains_tool "noai" "${SELECTED_TOOLS[@]}"; then
    write_file ".noai" "# Remove this file if you want JetBrains AI Assistant enabled for this project."
  fi

  if contains_tool "claude" "${SELECTED_TOOLS[@]}"; then
    write_file ".claude/settings.json" "$(build_claude_settings)"
  fi

  if contains_tool "codex" "${SELECTED_TOOLS[@]}"; then
    write_file "AGENTS.md" "$(build_agents_md)"
  fi

  if contains_tool "copilot" "${SELECTED_TOOLS[@]}"; then
    write_file ".github/copilot-instructions.md" "$(build_copilot_instructions)"
    write_ignore_file ".copilotignore" "$ignore_content"
  fi
}

main() {
  parse_args "$@"
  parse_tools "$TOOLS_CSV"

  log "Generating AI/CLI project files..."
  log "Profile: $PROFILE"
  log "Tools: ${SELECTED_TOOLS[*]}"

  generate_files
  prompt_global_gitignore_update

  if [[ "$DRY_RUN" -eq 1 ]]; then
    log "Dry run complete."
  else
    log "Done."
  fi
}

main "$@"
