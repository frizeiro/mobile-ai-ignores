#!/usr/bin/env bash

set -euo pipefail

SCRIPT_NAME="$(basename "$0")"

PROFILE="balanced"
FORCE=0
DRY_RUN=0
BACKUP=0
TOOLS_CSV="gemini,qwen,windsurf,jetbrains,claude,codex,copilot"
WRITTEN_FILES=()

usage() {
  cat <<EOF
Usage: $SCRIPT_NAME [options]

Generate AI/CLI ignore and instruction files for mobile projects.

Options:
  --profile <balanced|strict>  Choose rule intensity. Default: balanced
  --tools <csv>                Tools to generate. Default: $TOOLS_CSV
  --force                      Overwrite existing non-ignore files
  --backup                     Backup files to <file>.bak before writing
  --dry-run                    Show what would be created
  -h, --help                   Show this help

Supported tools:
  gemini       -> .geminiignore
  qwen         -> .qwenignore
  windsurf     -> .codeiumignore
  jetbrains    -> .aiignore
  noai         -> .noai
  claude       -> .claude/settings.json
  codex        -> AGENTS.md
  gpt          -> alias of codex
  openai       -> alias of codex
  copilot      -> .github/copilot-instructions.md, .copilotignore
  all          -> all of the above

Examples:
  $SCRIPT_NAME
  $SCRIPT_NAME --profile strict --tools gemini,qwen,windsurf,jetbrains,codex
  $SCRIPT_NAME --tools all --dry-run
EOF
}

log() {
  printf '%s\n' "$*"
}

warn() {
  printf 'Warning: %s\n' "$*" >&2
}

die() {
  printf 'Error: %s\n' "$*" >&2
  exit 1
}

contains_tool() {
  local needle="$1"
  shift
  local item
  for item in "$@"; do
    if [[ "$item" == "$needle" ]]; then
      return 0
    fi
  done
  return 1
}

normalize_tool() {
  case "$1" in
    gemini) printf 'gemini\n' ;;
    qwen) printf 'qwen\n' ;;
    windsurf|codeium) printf 'windsurf\n' ;;
    jetbrains|aiassistant|idea) printf 'jetbrains\n' ;;
    noai|jetbrains-noai) printf 'noai\n' ;;
    claude|anthropic) printf 'claude\n' ;;
    codex|gpt|openai) printf 'codex\n' ;;
    copilot|github) printf 'copilot\n' ;;
    all) printf 'all\n' ;;
    *) return 1 ;;
  esac
}

append_line() {
  local line="$1"
  if [[ -z "${BUFFER:-}" ]]; then
    BUFFER="$line"
  else
    BUFFER+=$'\n'"$line"
  fi
}

record_written_file() {
  local path="$1"

  if ! contains_tool "$path" "${WRITTEN_FILES[@]:-}"; then
    WRITTEN_FILES+=("$path")
  fi
}

